package Model;

import java.io.Serializable;

/**
 *
 * @author ADMIN
 */
public class Taisanthechap implements Serializable{
    private int id;
    private String ten;
    private String mota;

    public Taisanthechap() {
        super();
    }

    public Taisanthechap( String ten, String mota) {
        super();
        this.ten = ten;
        this.mota = mota;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }
    
}
